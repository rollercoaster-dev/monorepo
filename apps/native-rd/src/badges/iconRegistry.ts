/**
 * Get a Phosphor Icon component by name.
 * Returns undefined if the icon is not in the registry.
 */
export function getIconComponent(name: string): Icon | undefined {
  return ICON_REGISTRY[name];
}

/**
 * Get all registered icon names.
 */
export function getRegisteredIconNames(): string[] {
  return Object.keys(ICON_REGISTRY);
}
